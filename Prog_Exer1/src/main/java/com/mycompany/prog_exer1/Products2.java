    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// Get the selected row index
    int selectedRow = jTable1.getSelectedRow();

    // Check if a row is selected
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
    } else {
        // Get the data from the text fields
        String type = prodType.getText().trim();
        String desc = prodDESC.getText().trim();
        String sup = prodSUP.getText().trim();
        String quan = prodQUANT.getText().trim();
        String tc = prodTC.getText().trim();
        String date = prodDATE.getText().trim();

        // Validate the data
        if (type.isEmpty() || desc.isEmpty() || sup.isEmpty() || quan.isEmpty() || tc.isEmpty() || date.isEmpty())  {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Invalid Data", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean isValid = true; // Flag to track overall validity

            // Validate phone number
            if (!quan.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid digit.", "Invalid Quantity", JOptionPane.ERROR_MESSAGE);
                isValid = false;
            }

            // Validate email
            if (!tc.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid digit", "Invalid Total Cost", JOptionPane.ERROR_MESSAGE);
                isValid = false;
            }

            // Validate birthday
            if (!date.matches("^\\d{2}/\\d{2}/\\d{4}$")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid date (mm/dd/yyyy).", "Invalid Date", JOptionPane.ERROR_MESSAGE);
                isValid = false;
            }

            if (isValid) {
                // Update the row in the table with the new data
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setValueAt(type, selectedRow, 1);
                model.setValueAt(desc, selectedRow, 2);
                model.setValueAt(sup, selectedRow, 3);
                model.setValueAt(quan, selectedRow, 4);
                model.setValueAt(tc, selectedRow, 5);
                model.setValueAt(date, selectedRow, 6);

                // Display a message indicating the update
                JOptionPane.showMessageDialog(this, "Row updated successfully.", "Update Successful", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

