    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
 boolean hasError = false;
        
if (
    prodType.getText().isEmpty() || 
    prodDESC.getText().isEmpty() || 
    prodSUP.getText().isEmpty() || 
    prodQUANT.getText().isEmpty() || 
    prodDATE.getText().isEmpty()) {
    
    hasError = true;
    JOptionPane.showMessageDialog(this, "Fill all the empty boxes", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    if (qLabel.getForeground().equals(Color.RED) || 
        tcLabel.getForeground().equals(Color.RED) || 
        dLabel.getForeground().equals(Color.RED)) {
        
        hasError = true;
        JOptionPane.showMessageDialog(this, "Cannot Edit. Data is invalid.", "Error", JOptionPane.ERROR_MESSAGE);
    } 
    
    
if (!hasError) {  DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int rowCount = model.getRowCount();

    // Search for the first available ID starting from 1
    int id = 1;
    boolean found = false;
    while (!found) {
        found = true; // Assume the ID is available
        for (int i = 0; i < rowCount; i++) {
            int existingId = (int) model.getValueAt(i, 0);
            if (existingId == id) {
                found = false; // ID is already in use
                id++; // Try the next ID
                break;
            }
        }
    }

    // Get the values from the text fields
    String type = prodType.getText().trim();
    String desc = prodDESC.getText().trim();
    String sup = prodSUP.getText().trim();
    String quan = prodQUANT.getText().trim();
    String tc = prodTC.getText().trim();
    String date = prodDATE.getText().trim();

    // Create a new row for the customer table
    Object[] newRow = {id, type, desc, sup, quan, tc, date};
    model.addRow(newRow);

    // Display a message box
    JOptionPane.showMessageDialog(this, "Data saved successfully!", "Save Confirmation", JOptionPane.INFORMATION_MESSAGE);

    // Clear the text fields
    prodType.setText("");
    prodDESC.setText("");
    prodSUP.setText("");
    prodQUANT.setText("");
    prodTC.setText("");
    prodDATE.setText("");
    }
}

    
    }//GEN-LAST:event_saveActionPerformed

